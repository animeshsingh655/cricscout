export const getFormColor = (score) => {
  if (score >= 85) return '#39FF14'
  if (score >= 70) return '#ADFF2F'
  if (score >= 50) return '#FFD700'
  if (score >= 30) return '#FF4500'
  return '#FF3131'
}

export const getFormLabel = (score) => {
  if (score >= 85) return 'ELITE'
  if (score >= 70) return 'HIGH'
  if (score >= 50) return 'AVG'
  if (score >= 30) return 'LOW'
  return 'CRIT'
}

export const getPhaseColor = (label) => {
  const map = { elite: '#39FF14', high: '#ADFF2F', average: '#FFD700', low: '#FF4500', critical: '#FF3131' }
  return map[label] ?? '#94A3B8'
}

